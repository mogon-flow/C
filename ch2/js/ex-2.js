// Declare an array and initialize 10 items
var stars = [
  'Alpha',
  'Beta',
  'Gamma',
  'Delta',
  'Epsilon',
  'Theta',
  'Chi',
  'Zeta',
  'Mu',
  'Eta'
];
// Print the 1st item in the web console
console.log('The 1st item in stars: ' + stars[0] + '.');
// Print the 5th item in the web console
console.log('The 5th item in stars: ' + stars[4] + '.');
// Find the total of items and print the total
var size = stars.length;
console.log('The total number of items in stars is ' + size + '.');
